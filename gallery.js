/* ─── CAROUSEL ENGINE ─── */
// Everything below handles the slideshow — GALLERY_DATA above is all you edit

let carData = [];
let carIndex = 0;
let carTab = 'adult';
let autoTimer = null;

function buildCarousel(tabId) {
  const data = GALLERY_DATA[tabId];
  carTab = tabId;
  carIndex = 0;

  if (!data.files || data.files.length === 0) {
    carData = [{ src: '', placeholder: true, label: data.label }];
  } else {
    carData = data.files.map((f, i) => ({
      src: data.folder + f,
      placeholder: false,
      label: data.label + ' — ' + (i + 1) + ' / ' + data.files.length
    }));
  }
  renderCarSlide();
  renderDots();
  startAuto();
}

function renderCarSlide() {
  const stage = document.getElementById('carousel-stage');
  const counter = document.getElementById('car-counter');
  if (!stage) return;

  const item = carData[carIndex];
  counter.textContent = (carIndex + 1) + ' / ' + carData.length;

  if (item.placeholder) {
    stage.innerHTML = `<div class="car-placeholder"><span>📸</span><span>Add filenames to gallery.js to show photos</span></div>`;
    return;
  }

  // Fade transition
  stage.style.opacity = '0';
  setTimeout(() => {
    stage.innerHTML = `<img src="${item.src}" alt="${item.label}" class="car-img"
      onerror="this.outerHTML='<div class=car-placeholder><span>📷</span><span style=font-size:12px>${item.src.split('/').pop()}</span></div>'"
    />`;
    stage.style.opacity = '1';
  }, 120);

  // Update dots
  document.querySelectorAll('.car-dot').forEach((d, i) => {
    d.classList.toggle('active', i === carIndex);
  });
}

function renderDots() {
  const dotsEl = document.getElementById('car-dots');
  if (!dotsEl) return;
  const max = Math.min(carData.length, 12); // max 12 dots
  dotsEl.innerHTML = Array.from({length: max}, (_, i) =>
    `<span class="car-dot${i === 0 ? ' active' : ''}" onclick="carGoTo(${i})"></span>`
  ).join('');
  if (carData.length > 12) {
    dotsEl.innerHTML += `<span class="car-dot-more">+${carData.length - 12}</span>`;
  }
}

function carMove(dir) {
  carIndex = (carIndex + dir + carData.length) % carData.length;
  renderCarSlide();
  resetAuto();
}

function carGoTo(i) {
  carIndex = i;
  renderCarSlide();
  resetAuto();
}

function carExpand() {
  const item = carData[carIndex];
  if (!item || item.placeholder) return;
  openLightbox(carTab, carIndex);
}

function switchCarousel(tabId, btn) {
  document.querySelectorAll('.gtab').forEach(t => t.classList.remove('active'));
  btn.classList.add('active');
  buildCarousel(tabId);
}

function startAuto() {
  clearInterval(autoTimer);
  autoTimer = setInterval(() => {
    carIndex = (carIndex + 1) % carData.length;
    renderCarSlide();
  }, 4000);
}

function resetAuto() {
  clearInterval(autoTimer);
  startAuto();
}

// Swipe support for mobile
let touchStartX = 0;
document.addEventListener('DOMContentLoaded', function () {
  const stage = document.getElementById('carousel-stage');
  if (stage) {
    stage.addEventListener('touchstart', e => { touchStartX = e.touches[0].clientX; });
    stage.addEventListener('touchend', e => {
      const diff = touchStartX - e.changedTouches[0].clientX;
      if (Math.abs(diff) > 40) carMove(diff > 0 ? 1 : -1);
    });
  }

  // Keyboard
  document.addEventListener('keydown', e => {
    if (document.getElementById('lightbox') && document.getElementById('lightbox').classList.contains('open')) return;
    if (e.key === 'ArrowLeft')  carMove(-1);
    if (e.key === 'ArrowRight') carMove(1);
  });

  buildCarousel('adult');
});

/* ─── LIGHTBOX (for fullscreen expand) ─── */
let currentLightboxImages = [];
let currentLightboxIndex  = 0;

function openLightbox(tabId, index) {
  const data = GALLERY_DATA[tabId];
  if (!data || !data.files.length) return;
  currentLightboxImages = data.files.map((f, i) => ({
    src: data.folder + f,
    caption: data.label + ' — Photo ' + (i + 1) + ' of ' + data.files.length
  }));
  currentLightboxIndex = index;
  updateLightbox();
  document.getElementById('lightbox').classList.add('open');
  document.body.style.overflow = 'hidden';
}

function updateLightbox() {
  const lb  = document.getElementById('lightbox');
  const img = lb.querySelector('img');
  const cap = lb.querySelector('.lightbox-caption');
  const item = currentLightboxImages[currentLightboxIndex];
  img.src = item.src;
  cap.textContent = item.caption;
}

function closeLightbox() {
  document.getElementById('lightbox').classList.remove('open');
  document.body.style.overflow = '';
}

function lightboxPrev() {
  currentLightboxIndex = (currentLightboxIndex - 1 + currentLightboxImages.length) % currentLightboxImages.length;
  updateLightbox();
}

function lightboxNext() {
  currentLightboxIndex = (currentLightboxIndex + 1) % currentLightboxImages.length;
  updateLightbox();
}

document.addEventListener('DOMContentLoaded', function () {
  const lb = document.createElement('div');
  lb.id = 'lightbox';
  lb.className = 'lightbox';
  lb.innerHTML = `
    <button class="lightbox-close" onclick="closeLightbox()">✕</button>
    <button class="lightbox-nav lightbox-prev" onclick="lightboxPrev()">&#8249;</button>
    <img src="" alt="Gallery photo"/>
    <p class="lightbox-caption"></p>
    <button class="lightbox-nav lightbox-next" onclick="lightboxNext()">&#8250;</button>
  `;
  lb.addEventListener('click', e => { if (e.target === lb) closeLightbox(); });
  document.body.appendChild(lb);
});